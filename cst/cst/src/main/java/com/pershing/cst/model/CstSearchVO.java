package com.pershing.cst.model;

import java.util.Date;

public class CstSearchVO {
	private int issueNo;
	private String issueSupCategory;
	private String clientPriority;
	private String status;
	private String nextSteps;
	public int getIssueNo() {
		return issueNo;
	}
	public void setIssueNo(int issueNo) {
		this.issueNo = issueNo;
	}
	public String getIssueSupCategory() {
		return issueSupCategory;
	}
	public void setIssueSupCategory(String issueSupCategory) {
		this.issueSupCategory = issueSupCategory;
	}
	public String getClientPriority() {
		return clientPriority;
	}
	public void setClientPriority(String clientPriority) {
		this.clientPriority = clientPriority;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getNextSteps() {
		return nextSteps;
	}
	public void setNextSteps(String nextSteps) {
		this.nextSteps = nextSteps;
	}
	

}
